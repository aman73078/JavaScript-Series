//  while loop first check the condition then do the work.
let index = 1;
while(index <= 10){
    console.log(`while-loop print : ${index}`);
    index++;
};

// do-while loop first do the work then check the condition.
let i = 1
do{
    console.log(`do-while-loop print : 2*${i} =  ${2*i}`);
    i++;
}while(i <= 10);

let j = 11
do{
    console.log(`do-while-loop print : ${j}`);
    j++;
}while(j <= 10);